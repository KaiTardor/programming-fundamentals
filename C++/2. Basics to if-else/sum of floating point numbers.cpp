//Program that adds two floating point numbers.
#include<iostream>
using namespace std;
int main()
{
 float x,y,sum; //we declare float data type because we are adding floating point numbers.
 
 cout<<"Enter two floating point numbers\n"; // " \n " is used for a new line and is always used in double quotation.
 cin>>x>>y;
 sum=x+y; //formula for calculating sum.
 cout<<"Sum of floating point numbers="<<sum;
 system("pause");  
}
 
