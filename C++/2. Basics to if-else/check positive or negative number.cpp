// this is a program to check the number is positive or negative using if-else statements.
#include<iostream>
using namespace std;
int main()
{
int num;
 cout<<"Please enter the number"<<endl;
 cin>>num;
 if(num>=0)
 {
 cout<<"Number is positive"<<endl<<num<<endl;
 }
 else
 {
 cout<<"Number is negative"<<endl<<num<<endl;    
 }
 system("pause"); 
}
