//This is a program to convert miles into kilometers (1 mile=1.609km).
#include<iostream>
using namespace std;
int main()
{
 float miles,km;
 cout<<"Please Enter Miles";
 cin>>miles;
 km=miles*1.609; //Formula for converting miles into km.
 cout<<"Miles in Km are="<<km;
 system("pause");   
}
